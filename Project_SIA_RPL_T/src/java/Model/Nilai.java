public class Nilai {
    private String kode;
    private String nis;
    private int semester;
    private double kkm;
    private double nilaiTugas;
    private double nilaiHarian;
    private double nilaiUts;
    private double nilaiUas;
    private double nilaiSemester;
    private double nilaiAkhir;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNis() {
        return nis;
    }

    public void setNis(String nis) {
        this.nis = nis;
    }

    public int getSemester() {
        return semester;
    }

    public void setSemester(int semester) {
        this.semester = semester;
    }

    public double getKkm() {
        return kkm;
    }

    public void setKkm(double kkm) {
        this.kkm = kkm;
    }

    public double getNilaiTugas() {
        return nilaiTugas;
    }

    public void setNilaiTugas(double nilaiTugas) {
        this.nilaiTugas = nilaiTugas;
    }

    public double getNilaiHarian() {
        return nilaiHarian;
    }

    public void setNilaiHarian(double nilaiHarian) {
        this.nilaiHarian = nilaiHarian;
    }

    public double getNilaiUts() {
        return nilaiUts;
    }

    public void setNilaiUts(double nilaiUts) {
        this.nilaiUts = nilaiUts;
    }

    public double getNilaiUas() {
        return nilaiUas;
    }

    public void setNilaiUas(double nilaiUas) {
        this.nilaiUas = nilaiUas;
    }

    public double getNilaiSemester() {
        return nilaiSemester;
    }

    public void setNilaiSemester(double nilaiSemester) {
        this.nilaiSemester = nilaiSemester;
    }

    public double getNilaiAkhir() {
        return nilaiAkhir;
    }

    public void setNilaiAkhir(double nilaiAkhir) {
        this.nilaiAkhir = nilaiAkhir;
    }
    
}
